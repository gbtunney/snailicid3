export const schemaEntryKey: z.ZodType<
    LiteralUnion<(typeof ENTRY_KEY_DEFAULTS)[number], string>
> = z
    .union([z.enum(ENTRY_KEY_DEFAULTS), z.string()])
    .refine(
        (key): boolean => {
            const slug = key.startsWith('./') ? key.slice(2) : key
            return (
                arrayIncludes(ENTRY_KEY_DEFAULTS, key) ||
                SLUG_LIKE_REGEX.test(slug)
            )
        },
        {
            message:
                'Entry key must be root-like or slug-like (e.g. "./utils").',
        },
    )
    .refine((key) => !SOURCE_EXTENSION_REGEX.test(key), {
        message:
            'Entry key must not include a source file extension. Use sourceFile instead.',
    })
